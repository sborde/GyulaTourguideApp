public class UsefulPlaces {

    private String mName;

    private String mAddress;

    public UsefulPlaces(String mName, String mAddress) {
        this.mName = mName;
        this.mAddress = mAddress;
    }

    public String getmName() {
        return mName;
    }

    public String getmAddress() {
        return mAddress;
    }
}
